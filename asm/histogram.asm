MAIN:
  LDI  R4,PRINTLOOP
  CALL R4

  LDI  R4,1
  SHL  R1,R4
  LDI  R2,0
  LDI  R0,PRINTLIMIT
  LDI  R4,SETCOUNTERS
  CALL R4

  LDI  R0,MAINLIMIT
  LDI  R4,GETCOUNTERS
  CALL R4
  INC  R2
  CMP  R2,R1
  LDI  R0,MAINLIMIT
  LDI  R4,SETCOUNTERS
  CALL R4

  LDI  R3,13
  PRA  R3
  LDI  R4,MAIN
  JLT  R4
  HLT

PRINTLOOP:
  LDI  R3,42
  PRA  R3
  LDI  R0,PRINTLIMIT
  LDI  R4,GETCOUNTERS
  CALL R4
  INC  R2
  CMP  R2,R1
  LDI  R0,PRINTLIMIT
  LDI  R4,SETCOUNTERS
  CALL R4
  LDI  R4,PRINTLOOP
  JLT  R4
  RET

GETCOUNTERS:
  LD   R1,R0
  INC  R0
  LD   R2,R0
  RET

SETCOUNTERS:
  ST   R0,R1
  INC  R0
  ST   R0,R2
  RET

PRINTLIMIT:
  db   1

PRINTCOUNTER:
  db   0

MAINLIMIT:
  db   7

MAINCOUNTER:
  db   0